    {
        $items = DB::table('items')
            ->join('types', 'items.types_id', '=', 'types.id')
            ->join('targets', 'targets.id', '=', 'items.targets_id')
            ->select('items.name_of_item', 'targets.target', 'types.type', 'items.price', 'items.rating', 'items.img')
            ->get();

        // load the view and pass the plans
        return view('index')->with([
            'items' => $items,
        ]);
    }

